const mongoose = require('mongoose');

const submission = mongoose.Schema(
  {
    title: {
      type: String,
      required: true,
      trim: true
    },
    description: {
      type: String,
      required: true,
      trim: true
    },
    deadline: {
        type: Date,
        required: true,
    },
    file_path: {
      type: String,
      required: true
    },
    file_mimetype: {
      type: String,
      required: true
    }
  },
  {
    timestamps: true
  }
);

const Submission = mongoose.model('Submission', submission);

module.exports = Submission;